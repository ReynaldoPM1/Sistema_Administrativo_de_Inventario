    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            // TODO add your handling code here:
            PreparedStatement pps = cn.prepareStatement("INSERT INTO productos(id_producto,nombre_producto,descripcion_producto,cantidad_producto) VALUES(?,?,?,?)");
            pps.setString(1,txtID.getText());
            pps.setString(2,txtNombre.getText());
            pps.setString(3,txtDescripcion.getText());
            pps.setString(4,txtCantidad.getText());
            pps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Registro exitoso");
        } catch (SQLException ex) {
            Logger.getLogger(RegistrarProducto.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

