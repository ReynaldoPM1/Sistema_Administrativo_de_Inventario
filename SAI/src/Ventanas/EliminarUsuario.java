    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String clave = txtID.getText();
        try {
            String sql = "SELECT * FROM usuarios WHERE id_usuario = '" + clave +"'";
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            if(rs.next()==false){
                JOptionPane.showMessageDialog(null, "Â¡Usuario inexistente!");
            }
            else{
                PreparedStatement pps = cn.prepareStatement("DELETE FROM usuarios WHERE id_usuario = '" + clave + "'");
                pps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Operacion exitosa");
            }
        } catch (SQLException ex) {
            Logger.getLogger(VerProductos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

